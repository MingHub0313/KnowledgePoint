public class InnerClass {

	/**
	 * 内部类的种类:
	 * 	1.成员内部类
	 * 	2.静态内部类
	 * 	3.局部内部类
	 * 	4.匿名内部类
	 */

	/**
	 * 内部类的使用场景和优点:
	 * 优点: 每个内部类都能独立地继承一个类(实现多个接口),无论外部类是否已经继承或者实现,对于内部类都没有影响.内部类的存在使得Java的多继承变得更加完善
	 * 在开发设计中会存在一些使用接口很难解决的问题,而类却是能继承一个父类.这个时候可以利用内部类去继承其父类,及implements 多个接口能力来解决
	 * 内部类使得多重继承的解决方案变得更加完善.
	 */
}
